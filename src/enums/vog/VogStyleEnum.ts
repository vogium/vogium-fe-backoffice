  export const VogStyleOptions = () => {
    return Object.values(VogStyle).map((style) => ({
      label: VogStyleLabels[style],
      value: style,
    }));
  };
  
  export const getVogStyleOption = (value: VogStyle) => {
    return VogStyleOptions().find((option) => option.value === value);
  };